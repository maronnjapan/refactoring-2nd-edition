let user = {
  name: "John Doe",
  age: 30,
  email: "john.doe@example.com",
};

function getUserName() {
  return user.name;
}

function getUserAge() {
  return user.age;
}

function getUserEmail() {
  return user.email;
}

function setUserName(name) {
  user.name = name;
}

function setUserAge(age) {
  user.age = age;
}

function setUserEmail(email) {
  user.email = email;
}
